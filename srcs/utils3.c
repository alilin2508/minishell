#include "minishell.h"

char	*get_tmp_che(char *tmp, char *str, int *i, int *j)
{
	int		c;

	c = str[*i - 1];
	while (str[*i] != c)
	{
		if ((str[*i] == '>' || str[*i] == '<'))
		{
			if (c == '"')
			{
				tmp[*j] = '\\';
				*j += 1;
			}
			tmp[*j] = '\\';
			*j += 1;
		}
		tmp[*j] = str[*i];
		*j += 1;
		*i += 1;
	}
	return (tmp);
}
